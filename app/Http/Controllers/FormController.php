    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();

        // Remove empty strings in phone_restrictions array, remove repeated numbers
        if (!empty($input['details'])) {
            $input['details'] = array_filter($input['details'], static function ($item) {
                return !empty($item);
            });

            $input['details'] = array_unique($input['details']);
        }

        /** @var Promotion $promotion */
        $forms = Form::create($input);

        return redirect(route('forms.index'));
    }

